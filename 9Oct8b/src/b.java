class BaseClass {
    public void baseMethod() {
        System.out.println("This is the base class method.");
    }
}

// Subclass
class MySubclass extends BaseClass {
    // Method 1
    public void methodOne() {
        System.out.println("This is method one in the subclass.");
    }

    // Method 2
    public void methodTwo() {
        System.out.println("This is method two in the subclass.");
    }
}